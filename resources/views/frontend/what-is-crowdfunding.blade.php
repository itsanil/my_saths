<p>Crowdfunding is essentially the opposite of the mainstream approach to business funding. Traditionally, if you want to raise funds to start a business or launch a new product or fund a charity (NGO) you would need to put together a business plan, do your market research, get a prototype ready (for products) or get a pitch note in place for charities etc., and then sell your story to a limited pool or wealthy individuals or institutions whom you know. These funding sources included HNI's, corporates, banks, angel investors and venture capital firms thus limiting your options to a few key players. You can think of this fundraising approach as a funnel, with you and your campaign story at the wide end & your audience of probable funders at the narrow end. This is a very time consuming approach with massive geographical constraints and it only allows you to meet as much as funders on a daily basis.</p>
<p>A Crowdfunding platform is on the other hand, turns that funnel upside down. We give you (You can be an individual or an organisation) a single platform to build & showcase your campaign story to a large set of funders including your friends/family/fan base in a much quicker period of time. It transcends borders, you could be sitting in anywhere in the world & making approach globally. Its fast, efficient, transparent and effective. Imagine putting out your story & seeing results within hours & closing your fund raising in a days.</p>
<p>As there are many crowdfunding platform in India, Mysaathe gives you tools to tell your story well. We help you curate your story with our happiness team & give you tools, tips and advice to promote it on Social Media. We give you software & an elaborate dashboard to monitor progress of the funding through a backend tool, which also gives you your key analytics. This approach dramatically streamlines the traditional model of funding or donating.
